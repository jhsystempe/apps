package pe.gob.susalud.boleta.pago.model;

/**
 *
 * @author jllamas
 */
public class Personal {
    
    public String coEmpleado;
    public String tiDocumento;
    public String nuDocumento;
    public String noEmpleado;
    public String esDescarga;
    public String feDescarga;
    public String rutaArchivo;
    public String nuSecuencial;

    public Personal() {
    }

    public String getCoEmpleado() {
        return coEmpleado;
    }

    public void setCoEmpleado(String coEmpleado) {
        this.coEmpleado = coEmpleado;
    }

    public String getTiDocumento() {
        return tiDocumento;
    }

    public void setTiDocumento(String tiDocumento) {
        this.tiDocumento = tiDocumento;
    }

    public String getNuDocumento() {
        return nuDocumento;
    }

    public void setNuDocumento(String nuDocumento) {
        this.nuDocumento = nuDocumento;
    }

    public String getNoEmpleado() {
        return noEmpleado;
    }

    public void setNoEmpleado(String noEmpleado) {
        this.noEmpleado = noEmpleado;
    }

    public String getEsDescarga() {
        return esDescarga;
    }

    public void setEsDescarga(String esDescarga) {
        this.esDescarga = esDescarga;
    }

    public String getFeDescarga() {
        return feDescarga;
    }

    public void setFeDescarga(String feDescarga) {
        this.feDescarga = feDescarga;
    }

    public String getRutaArchivo() {
        return rutaArchivo;
    }

    public void setRutaArchivo(String rutaArchivo) {
        this.rutaArchivo = rutaArchivo;
    }

    public String getNuSecuencial() {
        return nuSecuencial;
    }

    public void setNuSecuencial(String nuSecuencial) {
        this.nuSecuencial = nuSecuencial;
    }
    
}
